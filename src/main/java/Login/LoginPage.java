    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String studentNum = studentNumField.getText();
        String studentPass = studentPasswordField.getText();
        
        try {
            String loginQuery = "SELECT * FROM Student_Login WHERE Student_Number = ? AND Student_Password = ?";
            pst = conn.prepareStatement(loginQuery);
            pst.setString(1, studentNum);
            pst.setString(2, studentPass);
            rs = pst.executeQuery();
            
            if (!rs.next()) {
                try {
                    String adminQuery = "SELECT * FROM Admin_Login WHERE Admin_Username = ? AND Password = ?";
                    pst = conn.prepareStatement(adminQuery);
                    pst.setString(1, (String) studentNumField.getText());
                    pst.setString(2, (String) studentPasswordField.getText());
                    rs = pst.executeQuery();
                    if (rs.next()) {
                        JOptionPane.showMessageDialog(null, "ADMIN LOGIN SUCCESSFUL");
                        AdminMainPage adminPage = new AdminMainPage();
                        adminPage.setVisible(true);
                        this.dispose();
                    }
                    else {
                        JOptionPane.showMessageDialog(null, "Account not found");
                    }
                } catch (Exception e) {
                }
            } else {
                JOptionPane.showMessageDialog(null, "Login Successful", "Login Successful", JOptionPane.INFORMATION_MESSAGE);
                StudentMainPage studentPage = new StudentMainPage();
                studentPage.setVisible(true);
                this.dispose();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

